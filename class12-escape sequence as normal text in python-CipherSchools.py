# If we want output: Line A \n Line B then just simply put double backslash instead of writing single backslash
print("Line A \\n Line B")

#If we want output: Line A \t Line B then just simply put double backslash instead of writing single backslash
print("Line A \\t Line B")

# We are putting extra backslash because we know (\\ = \)
print("this is 4 backslash \\\\\\\\")

# Now if we want output: \" \' then we will put 2 extra backslash because (\' = ' <-- a) and (\\ = \ <-- b) so (\\\' = \')
print("\\\" \\\'")