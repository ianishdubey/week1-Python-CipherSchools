#string methods part 1

name="Anish Dubey"

#1. len function <-- this will tell about the length of the string
length=len(name)
print(length)

#2. lower() function <-- this will gives the input string in small letters
print(name.lower())

#3. upper() function <-- this will gives the input string in capital letters
print(name.upper())

#4. title method <-- this method will gives string as output with its first letter as capital
print(name.title())

#5. count method <-- by this method we can find that how much times any letter is repeated in a word
print(name.count("h"))